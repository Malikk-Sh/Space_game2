function gw(s){
  let w=0;
  for(const ch of s.toUpperCase()){
    const g=GL[ch];
    w+=(!g||Array.isArray(g))?5:(g.w+1);
  }
  return w;
}
function txt(s,x,y,col,sc=1){
  cx.fillStyle=col;let px=x|0;y=y|0;
  for(const ch of s.toUpperCase()){
    const g=GL[ch];
    if(!g){px+=5*sc;continue;}
    if(Array.isArray(g)){
      // Стандартный 4-битный глиф (без изменений)
      for(let r=0;r<5;r++)for(let c=0;c<4;c++)
        if(g[r]&(8>>c))cx.fillRect(px+c*sc,y+r*sc,sc,sc);
      px+=5*sc;
    } else {
      // Широкий глиф {w, rows}
      const msb=1<<(g.w-1);
      for(let r=0;r<5;r++)for(let c=0;c<g.w;c++)
        if(g.rows[r]&(msb>>c))cx.fillRect(px+c*sc,y+r*sc,sc,sc);
      px+=(g.w+1)*sc;
    }
  }
}
function txs(s,x,y,col,shadow,sc=1){txt(s,x+1,y+1,shadow,sc);txt(s,x,y,col,sc);}
function txc(s,y,col,sc=1){txt(s,((LW-gw(s)*sc)/2)|0,y,col,sc);}
function txcs(s,y,col,shadow,sc=1){txs(s,((LW-gw(s)*sc)/2)|0,y,col,shadow,sc);}
